#include "libft.h"

int	ft_atoi(const char *str)
{
	int	posorneg;
	int	number;

	number = 0;
	posorneg = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			posorneg = posorneg * -1;
		str++;
	}
	while (*str >= 48 && *str <= 57)
	{
		number *= 10;
		number += *str - 48;
		str++;
	}
	return (number * posorneg);
}
